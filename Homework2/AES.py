# 키 확장 단계를 위한 Rcon
RCON = [
    0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36
]

# 16바이트 블록을 4x4 행렬로 변환 (열 단위 배치)
def BytesToMatrix(data):
    matrix = [[0] * 4 for _ in range(4)]
    for r in range(4):
        for c in range(4):
            matrix[r][c] = data[r + 4 * c]
    return matrix

# 4x4 행렬을 16바이트 블록으로 변환 
def MatrixToBytes(matrix):
    data = []
    for c in range(4):
        for r in range(4):
            data.append(matrix[r][c])
    return data

# SubBytes 단계
def SubBytes(matrix):
    for r in range(4):
        for c in range(4):
            matrix[r][c] = SBOX[matrix[r][c]]
    return matrix

# ShiftRows 단계 (1행 1칸, 2행 2칸, 3행 3칸 왼쪽 순환 이동)
def ShiftRows(matrix):
    for r in range(1, 4):
        matrix[r] = matrix[r][r:] + matrix[r][:r]
    return matrix

# GF(2^8) 상에서의 2 곱하기 연산 (xtime)
def xtime(a):
    return ((a << 1) ^ 0x1B) & 0xFF if (a & 0x80) else (a << 1) & 0xFF

# GF(2^8) 상에서의 3 곱하기 연산
def mul_3(a):
    return xtime(a) ^ a

# MixColumns 단계 (GF(2^8) 행렬 곱셈 반영)
def MixColumns(matrix):
    for c in range(4):
        a = matrix[0][c]
        b = matrix[1][c]
        d = matrix[2][c]
        e = matrix[3][c]

        matrix[0][c] = xtime(a) ^ mul_3(b) ^ d ^ e
        matrix[1][c] = a ^ xtime(b) ^ mul_3(d) ^ e
        matrix[2][c] = a ^ b ^ xtime(d) ^ mul_3(e)
        matrix[3][c] = mul_3(a) ^ b ^ d ^ xtime(e)
    return matrix

# AddRoundKey 단계
def AddRoundKey(matrix, key):
    for r in range(4):
        for c in range(4):
            matrix[r][c] ^= key[r][c]
    return matrix

# Key Expansion 단계
def KeyExpansion(key):
    key_matrix = BytesToMatrix(key)
    expanded_key = key_matrix.copy()

    rounds = 10

    # i가 4부터 43까지 총 40번의 열(Word)을 새로 생성
    for i in range(4, 4 * (rounds + 1)):
        temp = expanded_key[i - 1].copy()

        if i % 4 == 0:
            # RotWord: 순환 이동
            temp = temp[1:] + temp[:1]
            # SubWord: SBOX 치환
            temp = [SBOX[b] for b in temp]
            # Rcon 연산 (GF(2^8) 상에서 xOR)
            temp[0] ^= RCON[i // 4]

        new_row = [expanded_key[i - 4][j] ^ temp[j] for j in range(4)]
        expanded_key.append(new_row)

    # 44개의 Word(4x1)를 4개씩 묶어 라운드 키 행렬 리스트로 재구성
    round_keys = []
    for r in range(rounds + 1):
        round_keys.append(expanded_key[r*4 : (r+1)*4])
        
    return round_keys

# 암호화
def Encrypt(plaintext, key):
    matrix = BytesToMatrix(plaintext)
    round_keys = KeyExpansion(key)

    # 초기 AddRoundKey (Round 0)
    matrix = AddRoundKey(matrix, round_keys[0])

    # 1 ~ 9 라운드 반복
    for round in range(1, 10):
        matrix = SubBytes(matrix)
        matrix = ShiftRows(matrix)
        matrix = MixColumns(matrix)
        matrix = AddRoundKey(matrix, round_keys[round])

    # 마지막 10 라운드 (MixColumns 생략)
    matrix = SubBytes(matrix)
    matrix = ShiftRows(matrix)
    matrix = AddRoundKey(matrix, round_keys[10])

    return MatrixToBytes(matrix)

